; $F900
freezer_exec_menu:
      sei
      ldx  #$00                         ; Initialize DDR to make sure we can read keyboard
      stx  $DC03                        ; DDR B
      dex
      stx  $DC02                        ; DDR A
      stx  $05
      lda  #$80
      sta  $DC0E                        ; Initialize timer A
      sta  $DC0F                        ; Initialize timer B
      lda  #$7B                         ; Switch the VIC-II to illegal bitmap mode 1
      sta  $D011                        ; VIC control register
      lda  $DD00
      and  #$FC                         ; Switch the VIC-II to bank 3
      sta  $DD00
      ; We want a raster interrupt on scanline 1
      lda  #$01
      sta  $D012
      sta  $D01A
      sta  $D019
      lda  #$FF
      sta  $02
      jsr  colram_topline_green
      ; Screen RAM at $F000, charset at $F000
      lda  #$CC
      sta  $D018                        ; VIC memory control register
      ; Black background
      lda  #$00
      sta  $D021
      ; Delay loop
      ldy  #$00
:     dey
      bne  :-
WF942:
      jsr  colram_topline_green
      ldy  $03                          ; Currently selected pull-down menu
      ; Under normal circumstances we initiallize $03 (currently selected menu)
      ; with zero (backup menu), but in case we return from PRINT/VIEW $03
      ; already contains #$03 (print menu), so we leave it as it is.
      cpy  #$03                         ; Print menu?
      beq  WF951
      ldy  #$00
      sty  $03
menu_changed:
      ldy  $03
WF951:
      jsr  show_selected_menu
      lda  #$00
      sta  $D418                        ; Set volume to 0 to create a pop on the speakers
      cli
freezer_loop:
      lda  #$FF
      sta  $DC00                        ; Deselect all keyboard lines
      ldx  #$10
:     lda  $DC01                        ; Read gamee port 2
      cmp  #$FF
      bne  :+                           ; Joystick action? Then jump
      dex
      bne  :-
      lda  $DC00                        ; Data port A #1: keyboard, joystick, paddle, optical pencil
      cmp  #$FF
      bne  :+
      jmp  keyaction
:     cmp  $04                          ; Joystick changed?
      beq  freezer_loop
      jsr  joyaction
      bcc  jmp_freezer_action           ; Button pressed? Then jump
      txa
      beq  :+
      bpl  leftright
      jmp  left
:     tya
      beq  :+
      bpl  updown
      jmp  up
:     jmp  freezer_loop

keyaction:
      lda  #$FE                         ; Select keyboard column H
      sta  $DC00
:     lda  $DC01
      cmp  $DC01                        ; Wait for keyboard change
      bne  :-
      cmp  $04                          ; Changeed to previous value?
      beq  freezer_loop
      sta  $04
      cmp  #$F7                         ; F7 pressed?
      beq  jmp_freezer_action
      ldx  #$BF                         ; Select keyboard column B
      stx  $DC00
      ldx  $DC01
      cmp  #$7F                         ; CRSR UP/DOWN presset?
      beq  updown
      cmp  #$FB                         ; CRSR LEFT/RIGHT presset?
      beq  leftright
      jmp  freezer_loop
jmp_left:
      jmp  left

jmp_freezer_action:
      jmp  freezer_action1

updown:
      cpx  #$EF                         ; Right shift prsseed?
      beq  jmp_up
      jmp  down

leftright:
      cpx  #$EF                         ; Left shift pressed?
      beq  jmp_left
      jmp  right

jmp_up:
      jmp  up


